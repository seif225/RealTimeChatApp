package com.example.realtimechatapp;

public class Chat {
    private String name;
    private String message;


    public void setMessage(String message) {
        this.message = message;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getMessage() {
        return message;
    }

}
